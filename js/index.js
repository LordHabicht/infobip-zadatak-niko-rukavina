function showModal() {
    var backdrop = document.getElementById("backdrop");
    backdrop.style.display = "block";
    var modal =  document.getElementById("modal");
    modal.style.display = "block";
}

function hideModal() {
    var backdrop = document.getElementById("backdrop");
    backdrop.style.display = "none";
    var modal =  document.getElementById("modal");
    modal.style.display = "none";
}